package com.jiantong.pojo;

public class LinksSummary {

	/**标题*/
	private String title;
	
	/**超链接*/
	private String href;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getHref() {
		return href;
	}

	public void setHref(String href) {
		this.href = href;
	}
	
}
